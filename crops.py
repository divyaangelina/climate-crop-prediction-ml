data = pd.read_csv('Crop_recommendation.csv')

#split data into features (x) and target variable (y)
x = data.drop('label', axis=1)
y = data['label']

#convert crop labels to numerical values
from sklearn.preprocessing import LabelEncoder
label_encoder = LabelEncoder()
y = label_encoder.fit_transform(y)

#split data into training and testing sets
x_train, x_test, y_train, y_test = train_test_split(x, y, test_size=0.2, random_state=42)

#display first row of dataset
data.head()

#summarise data
print (data.describe())

#visualize data
sns.pairplot(data=data, hue='label')

#create logistic regression model
model = LogisticRegression(max_iter=int(1e6), tol=1e-6, solver = 'saga')

#train model on training data
model.fit(x_train, y_train)

#make predictions on testing data
y_pred = model.predict(x_test)

#calculate accuracy
accuracy = accuracy_score(y_test, y_pred)
print(f"Accuracy: {accuracy:.2f}")

#get feature coefficients
feature_coeffs = pd.Series(model.coef_[1], index=x.columns)

#visualize feature importance
plt.figure(figsize=(10, 5))
sns.barplot(x=feature_coeffs.abs(), y=feature_coeffs.index, orient='h')
plt.title("Feature Importance")
plt.ylabel("Features")
plt.xlabel("Magnitude")
plt.show()

classifier = LogisticRegression()
y_score = classifier.fit(x_train, y_train).predict_proba(x_test)

from sklearn.metrics import roc_curve, auc

y_prob = classifier.predict_proba(x_test)

fpr = {}
tpr = {}
roc_auc = {}

#loop through each class
for i, class_name in enumerate(classifier.classes_):
    fpr[i], tpr[i], _ = roc_curve(y_test == class_name, y_prob[:, i])
    roc_auc[i] = auc(fpr[i], tpr[i])

#plot ROC curves for each class
plt.figure(figsize=(8, 6))
for i, class_name in enumerate(classifier.classes_):
    plt.plot(fpr[i], tpr[i], lw=2, label=f'ROC curve for {class_name} (AUC = {roc_auc[i]:.2f})')

plt.plot([0, 1], [0, 1], color='blue', lw=3, linestyle='solid')
plt.xlim([0.0, 1.0])
plt.ylim([0.0, 1.05])
plt.xlabel('False Positive Rate')
plt.ylabel('True Positive Rate')
plt.legend(loc='center left', bbox_to_anchor=(1, 0.5))
plt.show()

feature_names = data.columns[:-1]
crop_names = data.iloc[:, -1].unique()

#calculate feature importance based on relative magnitude
for i, crop_name in enumerate(crop_names):
  feature_importance = model.coef_[i]/np.abs(model.coef_[i]).sum()


  #plot feature importance for all crops
  plt.figure(figsize=(10, 5))
  plt.barh(feature_names, feature_importance, color=['red' if coef < 0 else 'green' for coef in feature_importance])
  plt.xlabel('Relative Feature Importance')
  plt.ylabel('Features')
  plt.title(f'Feature Importance based on Relative Magnitude for {crop_name}')
  plt.show()