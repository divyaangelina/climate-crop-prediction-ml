data = pd.read_csv('Temperature.csv')

# Create 'ds' column from Year, Month, and Day
data['ds'] = pd.to_datetime(data[['Year', 'Month', 'Day']])
data = data.rename(columns={'Temperature': 'y', 'City': 'unique_id'})
data2 = data[['ds', 'y', 'unique_id']]

#locations = ['Bombay (Mumbai)', 'Calcutta', 'Chennai (Madras)', 'Delhi']

train = data2.loc[(data2['ds'] >= '1995-01-01') & (data2['ds'] < '2019-05-12') & (data2['unique_id'] == 'Calcutta')]
valid = data2.loc[(data2['ds'] >= '2019-05-12') & (data2['ds'] < '2020-05-12') & (data2['unique_id'] == 'Calcutta')]

model_1 = [RandomForestRegressor(random_state=0, n_estimators=100)]
model_2 = [ExtraTreesRegressor(random_state=0, n_estimators=100)]

model1 = MLForecast(models=model_1, freq='D', lags=[1], date_features=['dayofyear'], num_threads=6)
model2 = MLForecast(models=model_2, freq='D', lags=[1], date_features=['dayofyear'], num_threads=6)

# model.fit(train, id_col='unique_id', time_col='ds', target_col='y')
model1.fit(train)
model2.fit(train)

pred1 = model1.predict(2000)
pred2 = model2.predict(2000)

fig1 = plot_series(pd.concat([train, valid]), pred1)
fig1

fig2 = plot_series(pd.concat([train, valid]), pred2)
