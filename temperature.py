pred1_df = pd.DataFrame(pred1, columns=['pred1'])
pred2_df = pd.DataFrame(pred2, columns=['pred2'])

#combining predictions with validation data
valid_combined = valid.copy()
valid_combined['pred1'] = pred1_df['pred1']
