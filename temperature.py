rmse1 = np.sqrt(mse1)

#model 2
mse2 = mean_squared_error(valid_combined['y'], valid_combined['pred2'])
mae2 = mean_absolute_error(valid_combined['y'], valid_combined['pred2'])
rmse2 = np.sqrt(mse2)

print(f'Model 1 - MSE: {mse1:.4f}, MAE: {mae1:.4f}, RMSE: {rmse1:.4f}')
